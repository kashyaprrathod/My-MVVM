package com.kashyap.rathod.utils.date_time

/**
 * DateTimeUnits
 * Define units used by [DateTimeUtils.getDateDiff]
 * and also [DateTimeUtils.formatDate]
 *
 * @author thunder413
 * @version 1.0
 */
enum class DateTimeUnits {
    /**
     * Days
     */
    DAYS,

    /**
     * Hours
     */
    HOURS,

    /**
     * Minutes
     */
    MINUTES,

    /**
     * Seconds
     */
    SECONDS,

    /**
     * Milliseconds
     */
    MILLISECONDS
}